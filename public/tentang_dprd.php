            <h1><i class="fas fa-landmark"></i> Tentang Dewan Perwakilan Rakyat Daerah (DPRD)</h1>
            
            <h2>Pengertian DPRD</h2>
            <p>Dewan Perwakilan Rakyat Daerah (DPRD) adalah lembaga perwakilan rakyat yang berkedudukan sebagai unsur penyelenggara pemerintahan daerah. DPRD merupakan lembaga legislatif di tingkat daerah yang memiliki peran penting dalam sistem pemerintahan dan pembangunan daerah.</p>

            <h2>Fungsi dan Wewenang DPRD</h2>
            <p>DPRD memiliki tiga fungsi utama dalam menjalankan tugasnya:</p>
            
            <h3>1. Fungsi Legislasi</h3>
            <ul>
                <li>Membentuk Peraturan Daerah (Perda) bersama dengan Kepala Daerah</li>
                <li>Membahas dan menyetujui rancangan Perda</li>
                <li>Mengawasi pelaksanaan Perda dan peraturan perundang-undangan lainnya</li>
            </ul>

            <h3>2. Fungsi Anggaran</h3>
            <ul>
                <li>Membahas dan menyetujui APBD (Anggaran Pendapatan dan Belanja Daerah)</li>
                <li>Mengawasi pelaksanaan APBD</li>
                <li>Membahas laporan pertanggungjawaban APBD</li>
            </ul>

            <h3>3. Fungsi Pengawasan</h3>
            <ul>
                <li>Mengawasi pelaksanaan Perda dan peraturan perundang-undangan lainnya</li>
                <li>Mengawasi pelaksanaan APBD</li>
                <li>Mengawasi kebijakan Pemerintah Daerah</li>
            </ul>

            <h2>Hak DPRD</h2>
            <ul>
                <li><strong>Hak Interpelasi</strong>: Hak untuk meminta keterangan kepada Kepala Daerah</li>
                <li><strong>Hak Angket</strong>: Hak untuk melakukan penyelidikan</li>
                <li><strong>Hak Menyatakan Pendapat</strong>: Hak untuk menyatakan pendapat terhadap kebijakan Kepala Daerah</li>
            </ul>

            <h2>Struktur Organisasi</h2>
            <p>DPRD dipimpin oleh seorang Pimpinan yang terdiri dari:</p>
            <ul>
                <li>Ketua DPRD</li>
                <li>Wakil Ketua DPRD</li>
                <li>Sekretaris DPRD</li>
            </ul>

            <p>DPRD juga memiliki berbagai komisi dan badan yang menangani bidang-bidang tertentu seperti:</p>
            <ul>
                <li>Komisi A (Pemerintahan)</li>
                <li>Komisi B (Ekonomi dan Keuangan)</li>
                <li>Komisi C (Pembangunan)</li>
                <li>Komisi D (Kesejahteraan Rakyat)</li>
                <li>Badan Anggaran</li>
                <li>Badan Legislasi Daerah</li>
                <li>Badan Kehormatan</li>
            </ul>

            <h2>Masa Jabatan</h2>
            <p>Anggota DPRD memiliki masa jabatan selama 5 (lima) tahun dan dapat dipilih kembali untuk satu kali masa jabatan berikutnya.</p>

            <h2>Hubungan dengan Masyarakat</h2>
            <p>DPRD memiliki kewajiban untuk:</p>
            <ul>
                <li>Menyerap, menghimpun, menampung, dan menindaklanjuti aspirasi masyarakat</li>
                <li>Memberikan pertanggungjawaban secara moral dan politis kepada masyarakat</li>
                <li>Meningkatkan kesejahteraan masyarakat di daerah</li>
                <li>Menjadi jembatan komunikasi antara pemerintah dan masyarakat</li>
            </ul>

            <h2>Peran dalam Pembangunan Daerah</h2>
            <p>DPRD berperan aktif dalam pembangunan daerah melalui:</p>
            <ul>
                <li>Penyusunan program dan prioritas pembangunan</li>
                <li>Pengawasan pelaksanaan program pembangunan</li>
                <li>Evaluasi hasil pembangunan</li>
                <li>Penyusunan kebijakan yang mendukung pembangunan berkelanjutan</li>
            </ul>

