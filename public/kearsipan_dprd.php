            <h1><i class="fas fa-archive"></i> Sistem Kearsipan DPRD</h1>
            
            <h2>Pengertian Kearsipan</h2>
            <p>Kearsipan adalah hal-hal yang berkenaan dengan arsip, yang meliputi kegiatan penciptaan, penggunaan, pemeliharaan, dan penyusutan arsip dalam suatu sistem kearsipan yang menyeluruh. Sistem kearsipan DPRD dirancang untuk memastikan bahwa semua dokumen dan informasi penting dapat dikelola dengan baik, aman, dan mudah diakses ketika dibutuhkan.</p>

            <h2>Tujuan Sistem Kearsipan DPRD</h2>
            <ul>
                <li>Menjamin ketersediaan arsip yang autentik dan terpercaya</li>
                <li>Menjamin pelestarian arsip sebagai bahan pertanggungjawaban nasional</li>
                <li>Menjamin perlindungan kepentingan negara dan hak-hak keperdataan rakyat</li>
                <li>Menunjang penyelenggaraan pemerintahan</li>
                <li>Meningkatkan kualitas pelayanan publik</li>
                <li>Menjamin keamanan dan keselamatan arsip sebagai identitas dan jati diri bangsa</li>
            </ul>

            <h2>Jenis-Jenis Arsip di DPRD</h2>
            
            <h3>1. Arsip Vital</h3>
            <p>Arsip yang keberadaannya merupakan persyaratan dasar bagi kelangsungan operasional pencipta arsip, tidak dapat diperbarui, dan tidak tergantikan apabila rusak atau hilang.</p>
            <ul>
                <li>Akta pendirian organisasi</li>
                <li>Dokumen legal dan kontrak penting</li>
                <li>Keputusan-keputusan strategis</li>
                <li>Dokumen kepemilikan aset</li>
            </ul>

            <h3>2. Arsip Aktif</h3>
            <p>Arsip yang secara langsung dan terus menerus diperlukan dan digunakan dalam penyelenggaraan administrasi sehari-hari.</p>
            <ul>
                <li>Surat-menyurat yang sedang berjalan</li>
                <li>Dokumen proyek yang sedang berlangsung</li>
                <li>Berkas administrasi aktif</li>
                <li>Dokumen kegiatan rutin</li>
            </ul>

            <h3>3. Arsip Inaktif</h3>
            <p>Arsip yang frekuensi penggunaannya untuk penyelenggaraan administrasi sudah menurun.</p>
            <ul>
                <li>Proyek-proyek yang telah selesai</li>
                <li>Laporan-laporan periode sebelumnya</li>
                <li>Kontrak yang telah berakhir</li>
                <li>Dokumen yang sudah tidak aktif digunakan</li>
            </ul>

            <h2>Proses Pengelolaan Arsip</h2>
            
            <h3>1. Penciptaan Arsip</h3>
            <ul>
                <li>Pembuatan dokumen berdasarkan kegiatan organisasi</li>
                <li>Penerimaan dokumen dari pihak eksternal</li>
                <li>Registrasi dan pemberian nomor arsip</li>
            </ul>

            <h3>2. Penggunaan Arsip</h3>
            <ul>
                <li>Distribusi dokumen kepada pihak yang berwenang</li>
                <li>Penyimpanan dengan sistem yang terorganisir</li>
                <li>Peminjaman dan pengembalian arsip</li>
            </ul>

            <h3>3. Pemeliharaan Arsip</h3>
            <ul>
                <li>Perawatan fisik dokumen</li>
                <li>Digitalisasi dokumen penting</li>
                <li>Perlindungan dari kerusakan dan kehilangan</li>
            </ul>

            <h3>4. Penyusutan Arsip</h3>
            <ul>
                <li>Pemindahan arsip dari aktif ke inaktif</li>
                <li>Pemusnahan arsip yang telah habis masa simpannya</li>
                <li>Penyerahan arsip statis ke lembaga kearsipan</li>
            </ul>

            <h2>Sistem Digital DPRD</h2>
            <p>DPRD telah mengimplementasikan sistem kearsipan digital yang memungkinkan:</p>
            <ul>
                <li><strong>Pengajuan Online</strong>: Masyarakat dapat mengajukan berkas melalui website</li>
                <li><strong>Tracking Status</strong>: Monitoring real-time status pengajuan</li>
                <li><strong>Notifikasi Otomatis</strong>: Pemberitahuan melalui email</li>
                <li><strong>Backup Otomatis</strong>: Keamanan data dengan backup berkala</li>
                <li><strong>Akses Terkontrol</strong>: Sistem keamanan berlapis</li>
            </ul>

